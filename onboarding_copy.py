def split_completion_signal(reply: str) -> tuple[str, bool]:
    """Remove the internal completion marker before participant delivery."""
    complete = "INTERVIEW_COMPLETE" in reply
    return reply.replace("INTERVIEW_COMPLETE", "").strip(), complete
